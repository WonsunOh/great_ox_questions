/// 앱 라우트 경로 상수 정의
abstract class RoutePaths {
  /// 홈 화면
  static const String home = '/';

  /// 퀴즈 화면
  static const String quiz = '/quiz';

  /// 결과 화면
  static const String result = '/result';
}

/// 라우트 이름 상수 (Named Routes)
abstract class RouteNames {
  static const String home = 'home';
  static const String quiz = 'quiz';
  static const String result = 'result';
}
